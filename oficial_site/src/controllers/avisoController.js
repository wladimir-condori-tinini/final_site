function publicar(req, res) {
    var titulo = req.body.assuntoVar;
    var link_img = req.params.img_linkVar;
    var categoria = req.params.categoriaVar;
    var descricao = req.body.descricaoVar;
    var fk_usuario = req.body.fk_usuarioVar;

    if (titulo == undefined) {
        res.status(400).send("O título está indefinido!");
    } else if (link_img == undefined) {
        res.status(400).send("O link está indefinido!");
    } else if(categoria == undefined){
        res.status(400).send("A categoria está indefinido")
    }else if (descricao == undefined) {
        res.status(403).send("A descrição está indefinido!");
    } else if(fk_usuario == undefined){
        res.status(400).send("A fk_usuario está indefinido!")
    }else {
        avisoModel.publicar(titulo, link_img, categoria, descricao, fk_usuario)
            .then(
                function (resultado) {
                    res.json(resultado);
                }
            )
            .catch(
                function (erro) {
                    console.log(erro);
                    console.log("Houve um erro ao realizar o post: ", erro.sqlMessage);
                    res.status(500).json(erro.sqlMessage);
                }
            );
    }
}

